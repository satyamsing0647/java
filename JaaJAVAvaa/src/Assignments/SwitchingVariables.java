public class SwitchingVariables {
	public static void main(String[] args) {
		
		int a=10,b=20;
		System.out.println("Before swapping : " + a + "\t" + b );
		a=b;
		b=a;
		System.out.println("After swapping : " + a + "\t" + b);
		
	}
}
